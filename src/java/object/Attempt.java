
package object;


public class Attempt {

    private String codeAttempt;
    private String codeUser;
    private String nameUser;
    private String result;
    private String codeQuestion;
    private String codeAnswer;

    public String getCodeAttempt() {
        return codeAttempt;
    }

    public void setCodeAttempt(String codeAttempt) {
        this.codeAttempt = codeAttempt;
    }

    public String getCodeUser() {
        return codeUser;
    }

    public void setCodeUser(String codeUser) {
        this.codeUser = codeUser;
    }

    public String getNameUser() {
        return nameUser;
    }

    public void setNameUser(String nameUser) {
        this.nameUser = nameUser;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getCodeQuestion() {
        return codeQuestion;
    }

    public void setCodeQuestion(String codeQuestion) {
        this.codeQuestion = codeQuestion;
    }

    public String getCodeAnswer() {
        return codeAnswer;
    }

    public void setCodeAnswer(String codeAnswer) {
        this.codeAnswer = codeAnswer;
    }
    }
